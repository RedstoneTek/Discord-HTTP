package com.tek.dttp.struct.embed;

public class EmbedImage {
	
	private String url;
	private String proxy_url;
	private int height;
	private int width;
	
	public String getUrl() {
		return url;
	}
	
	public String getProxyUrl() {
		return proxy_url;
	}
	
	public int getHeight() {
		return height;
	}
	
	public int getWidth() {
		return width;
	}
	
}
