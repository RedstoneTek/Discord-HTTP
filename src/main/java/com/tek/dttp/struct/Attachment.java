package com.tek.dttp.struct;

public class Attachment {
	
	private String id;
	private String filename;
	private int size;
	private String url;
	private String proxy_url;
	private int height;
	private int width;
	
	public String getId() {
		return id;
	}
	
	public String getFilename() {
		return filename;
	}
	
	public int getSize() {
		return size;
	}
	
	public String getUrl() {
		return url;
	}
	
	public String getProxyUrl() {
		return proxy_url;
	}
	
	public int getHeight() {
		return height;
	}
	
	public int getWidth() {
		return width;
	}
	
}
